class BlogPost:
    def __init__(
        self,
        title: str,
        link: str,
        blogger_name: str,
        post_date: str,
        festival_kopis_id: str,
    ):
        self.title = title
        self.link = link
        self.blogger_name = blogger_name
        self.post_date = post_date
        self.festival_kopis_id = festival_kopis_id
